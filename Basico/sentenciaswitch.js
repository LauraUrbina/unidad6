function funcionswitch(a){
    switch(a){
        case 'rosa':
            console.log("se venden por docena y unidad")
            break
        case 'girasol':
            console.log("se venden por unidad")
            break
        case 'hortensia':
            console.log("se vende por ramos")
            break
        default:
            console.log("se venden por parejas")
    }
}

